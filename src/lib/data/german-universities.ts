// Function to filter universities based on search term
export function searchUniversities(searchTerm: string, limit: number = 10): string[] {
  if (!searchTerm || searchTerm.length < 2) {
    return GERMAN_UNIVERSITIES.slice(0, limit)
  }

  const lowerSearch = searchTerm.toLowerCase()

  // Exact matches first
  const exactMatches = GERMAN_UNIVERSITIES.filter(uni =>
    uni.toLowerCase().startsWith(lowerSearch)
  )

  // Then partial matches
  const partialMatches = GERMAN_UNIVERSITIES.filter(uni =>
    !uni.toLowerCase().startsWith(lowerSearch) &&
    uni.toLowerCase().includes(lowerSearch)
  )

  return [...exactMatches, ...partialMatches].slice(0, limit)
}
